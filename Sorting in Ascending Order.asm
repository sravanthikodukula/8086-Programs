.MODEL SMALL
.STACK 100
.DATA
NUM DW 0102H,0154H,0070H,0005H
COUNT DW 04H
.CODE
START :
MOV AX,@DATA
MOV DS,AX
MOV DX,COUNT
DEC DX
AGAIN :
LEA SI,NUM
MOV CX,DX
UP :
MOV AX,[SI]
CMP AX,[SI+2]
JNC NEXT
XCHG [SI+2],AX
MOV [SI],AX
NEXT :
INC SI
INC SI
LOOP UP
DEC DX
JNZ AGAIN
MOV AH,4CH
INT 21H
END START
END